// ── Ordena por fecha de salida: primero las más próximas ──────────────────
const MONTH_IDX: Record<string, number> = {
  ene: 0, feb: 1, mar: 2, abr: 3, may: 4, jun: 5,
  jul: 6, ago: 7, sep: 8, oct: 9, nov: 10, dic: 11,
};

function parseSortDate(dateStr: string): number {
  const lower = dateStr.toLowerCase();
  // "13 abr – 4 may 2026" → usa el primer grupo día+mes+año
  const m = lower.match(/(\d+)\s+([a-záéíóú]+)[^a-z\d]*(\d{4})/);
  if (m) {
    const day = parseInt(m[1]);
    const month = MONTH_IDX[m[2].slice(0, 3)] ?? 0;
    const year = parseInt(m[3]);
    return new Date(year, month, day).getTime();
  }
  // Solo año → se va al final (31/12 del año)
  const year = dateStr.match(/(\d{4})/)?.[1];
  if (year) return new Date(parseInt(year), 11, 31).getTime();
  return Infinity;
}

function sortByDeparture(list: Destination[]): Destination[] {
  return [...list].sort(
    (a, b) => parseSortDate(a.departureDate) - parseSortDate(b.departureDate)
  );
}

export const FEATURED_2026 = sortByDeparture(
  DESTINATIONS.filter((d) => d.year === 2026 && d.featured)
);

export const ALL_2026 = sortByDeparture(
  DESTINATIONS.filter((d) => d.year === 2026)
);

export const ALL_2025 = DESTINATIONS.filter((d) => d.year === 2025);

export function getDestinationBySlug(slug: string): Destination | undefined {
  return DESTINATIONS.find((d) => d.slug === slug);
}

export function getDestinationsByRegion(region: string): Destination[] {
  return sortByDeparture(
    DESTINATIONS.filter((d) => d.region === region && d.active)
  );
}
